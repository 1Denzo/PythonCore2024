A = input("Введите переменную A: ") # Получаем от пользователя переменную А
B = input("Введите переменную B: ") # Получаем от пользователя переменную B
C = int(A) % int(B) # Интрерпритируем A и B как числа, вычисляем останок от деления A на B - C
print("Целый остаток от деления A на B: ", C) # Выводим С